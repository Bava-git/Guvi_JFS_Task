package JFS_Task_1;

public class Question_01 {

	public static void main(String[] args) {

		// Write a program to print the number 10 to 50

		// Initiating a For loop to print numbers from 10 to 50
		for (int i = 10; i <= 50; i++) {
			System.out.println(i);
		}

		// Exit from program
		System.exit(0);

	}

}
