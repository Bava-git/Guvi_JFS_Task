package JFS_Task_1;

public class Question_01 {

	public static void main(String[] args) {
		
		// Write a program to print the number 10 to 50
		
		// init for loop 
		// init value for i is 10 and end when i reach to 50
		for(int i=10; i<=50; i++) {
			System.out.println(i);
		}
		
		// Exit from program
		System.exit(0);

	}

}
